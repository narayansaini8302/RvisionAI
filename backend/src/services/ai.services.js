const { GoogleGenerativeAI } = require("@google/generative-ai");
const puppeteer = require("puppeteer");

const genAI = new GoogleGenerativeAI(process.env.GEMINI_API_KEY);

/* -------------------- MODEL CONFIG -------------------- */

const MODEL_CONFIGS = [
    { name: "models/gemini-flash-latest", priority: 1 },
    { name: "models/gemini-2.0-flash-lite", priority: 2 },
    { name: "models/gemini-2.5-flash", priority: 3 },
    { name: "models/gemini-2.5-pro", priority: 4 }
];

/* -------------------- RETRY -------------------- */

async function callWithRetry(fn, attempts = 3) {
    for (let i = 1; i <= attempts; i++) {
        try {
            return await fn();
        } catch (err) {
            console.log(`❌ Attempt ${i} failed:`, err.message);
            if (i === attempts) throw err;
            await new Promise(r => setTimeout(r, i * 1000));
        }
    }
}

/* -------------------- SAFE JSON PARSE -------------------- */

function safeJSONParse(text) {
    try {
        const match = text.match(/\{[\s\S]*\}/);
        return match ? JSON.parse(match[0]) : null;
    } catch {
        return null;
    }
}

/* -------------------- PDF GENERATOR (FIXED FOR RENDER) -------------------- */

async function generatePdfFromHtml(html) {
    let browser;

    try {
        console.log("📄 Generating PDF...");

        // 🔥 FIXED: Use puppeteer with proper Render configuration
        browser = await puppeteer.launch({
            headless: "new",
            args: [
                "--no-sandbox",
                "--disable-setuid-sandbox",
                "--disable-dev-shm-usage",
                "--disable-gpu",
                "--disable-software-rasterizer",
                "--single-process",
                "--no-zygote"
            ],
            // Let puppeteer find Chrome automatically after postinstall
        });

        const page = await browser.newPage();
        await page.setContent(html, { 
            waitUntil: "networkidle0",
            timeout: 30000 
        });

        const pdf = await page.pdf({
            format: "A4",
            printBackground: true,
            margin: {
                top: "20mm",
                bottom: "20mm",
                left: "15mm",
                right: "15mm"
            }
        });

        console.log("✅ PDF success");
        return pdf;

    } catch (err) {
        console.error("❌ Puppeteer failed:", err.message);
        
        // Return a text-based PDF fallback
        const fallbackContent = `PDF Generation Failed\n\nReason: ${err.message}\n\nPlease try again later.`;
        return Buffer.from(fallbackContent);
    } finally {
        if (browser) await browser.close();
    }
}

